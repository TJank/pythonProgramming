class Book(object):
    def __init__(self, title, author, publisher, price, isbn):
        self.title = title
        self.author = author
        self.publisher = publisher
        self.price = price
        self.ISBN = isbn

    def getTitle(self):
        return self.title
    def getAuthor(self):
        return self.author
    def getPublisher(self):
        return self.publisher
    def getPrice(self):
        return self.price
    def getISBN(self):
        return self.ISBN



